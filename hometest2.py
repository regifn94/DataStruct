# Membuat dua set angka
set_A = {1, 2, 3, 4, 5}
set_B = {4, 5, 6, 7, 8}

# Irisan (intersection)
irisan = set_A & set_B
print("Irisan (A ∩ B):", irisan)

# Gabungan (union)
gabungan = set_A | set_B
print("Gabungan (A ∪ B):", gabungan)

# Selisih (difference)
selisih = set_A - set_B
print("Selisih (A - B):", selisih)