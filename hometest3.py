# Membuat dictionary mahasiswa
mahasiswa = {101: 'Andi', 102: 'Budi', 103: 'Citra'}
print("Data Mahasiswa:", mahasiswa)

# Menambahkan satu mahasiswa
mahasiswa[104] = 'Dewi'
print("Setelah menambah mahasiswa:", mahasiswa)

# Menghapus mahasiswa berdasarkan NIM
del mahasiswa[102]
print("Setelah menghapus mahasiswa dengan NIM 102:", mahasiswa)
